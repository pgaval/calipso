package gr.abiss.calipso.domain;

public class CommonStatus {
	private int id;
	private String description;
	private boolean active;
	private boolean visibleInSearch;
	
	public CommonStatus(int id, String description, boolean active, boolean visibleInSearch) {
		this.id = id;
		this.description = description;
		this.active = active;
		this.visibleInSearch = visibleInSearch;
	}

	public CommonStatus() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public boolean isActive() {
		return active;
	}

	public void setActive(boolean active) {
		this.active = active;
	}

	public boolean isVisibleInSearch() {
		return visibleInSearch;
	}

	public void setVisibleInSearch(boolean visibleInSearch) {
		this.visibleInSearch = visibleInSearch;
	}
	
}
